    private void tampilSiswa() {

        try {

            String sql = "SELECT nama_siswa FROM siswa";

            PreparedStatement ps = conn.prepareStatement(sql);

            ResultSet rs = ps.executeQuery();

            cmbSiswa.removeAllItems();

            while (rs.next()) {

                cmbSiswa.addItem(rs.getString("nama_siswa"));

            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(this,
                    "Gagal tampil data siswa\n" + e.getMessage());

        }

    }

    private void resetForm() {

        dateTanggal.setDate(null);

        if (cmbSiswa.getItemCount() > 0) {

            cmbSiswa.setSelectedIndex(0);

        }

        cmbStatus.setSelectedIndex(0);

    }

