    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if(dateTanggal.getDate()==null){

            JOptionPane.showMessageDialog(this,
                    "Tanggal belum dipilih");

            return;

        }
        
        try {

            String sql = "INSERT INTO kehadiran VALUES (NULL,?,?,?)";

            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, cmbSiswa.getSelectedItem().toString());

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

            String tanggal = sdf.format(dateTanggal.getDate());

            ps.setString(2, tanggal);

            ps.setString(3, cmbStatus.getSelectedItem().toString());

            ps.executeUpdate();

            JOptionPane.showMessageDialog(this,
                    "Data Kehadiran Berhasil Disimpan");

            resetForm();

        } catch (Exception e) {

            JOptionPane.showMessageDialog(this,
                    "Data Kehadiran Gagal Disimpan\n" + e.getMessage());

        }
    }//GEN-LAST:event_btnSimpanActionPerformed

